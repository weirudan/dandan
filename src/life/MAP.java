	    private int[][] nextStatus = new int[cell_map.length][cell_map[0].length];  
	  
	    private int[][] tempStatus = new int[cell_map.length][cell_map[0].length];  
	  
	    private Timer timer;  
	  
	    // 动画帧之间的延时，每秒60帧  
	    private final int DELAY_TIME = 1200;  
	  
	    public MAP() {
	        this.startAnimation();  
	    }  
	 /*随即修改对比图形
	  * 
	  */
	    protected void chang_map() { 
	    int i=0,j=0;
	   do{
		   i=(int) (Math.random()*14);
		   j=(int) (Math.random()*50);
	   }while(cell_map[i][j]==0);
	   cell_map[i][j]=0;
	    }
	    /* 
	     * 画图形界面 
	     *  
	     */  
	    @Override  
	    protected void paintComponent(Graphics g) {  
	        super.paintComponent(g);
	        chang_map();
	        
	        for (int i = 0; i < nextStatus.length; i++) {  
	            for (int j = 0; j < nextStatus[i].length; j++) {  
	                if (nextStatus[i][j] == 1) {  
	                    g.fillRect( j * width, i * height, width, height);  
	                   
	                } else {  
	                	 g.drawRect( j * width, i * height, width, height); 
	                }  
	            }  
	        }  
	    }  
	  
	    /** 
	     * 改变细胞状态 
	     *  
	        */  
	    private void changeCellStatus() {  
	        for (int row = 0; row < nextStatus.length; row++) {  
	            for (int col = 0; col < nextStatus[row].length; col++) {  
	  
	                switch (neighborsCount(row, col)) {  
	                case 0:  
	                case 1:  
	                case 4:  
	                case 5:  
	                case 6:  
	                case 7:  
	                case 8:  
	                    nextStatus[row][col] = 0;  
	                    break;  
	                case 2:  
	                    nextStatus[row][col] = tempStatus[row][col];  
	                    break;  
	                case 3:  
	                    nextStatus[row][col] = 1;  
	                    break;  
	                }  
	            }  
	        }  
	        copyWorldMap();  
	    }  
	  
	    /** 
	     * 获取当前坐标点临近细胞个数 
	     *  
	         */  
	    private int neighborsCount(int row, int col) {  
	        int count = 0, r = 0, c = 0;  
	  
	        for (r = row - 1; r <= row + 1; r++) {  
	            for (c = col - 1; c <= col + 1; c++) {  
	                if (r < 0 || r >= tempStatus.length || c < 0  
	                        || c >= tempStatus[0].length) {  
	                    continue;  
	                }  
	                if (tempStatus[r][c] == 1) {  
	                    count++;  
	                }  
	            }  
	        }  
	        if (tempStatus[row][col] ==1) {  
	            count--;  
	        }  
	        return count;  
	    }  
	  
	    /** 
	     * 开始动画 
	     *  
	         */  
	    private void startAnimation() {  
	    	this.chang_map();
	        for (int row = 0; row < cell_map.length; row++) {  
	            for (int col = 0; col < cell_map[0].length; col++) {  
	                nextStatus[row][col] = cell_map[row][col];  
	                tempStatus[row][col] = cell_map[row][col];  
	            }  
	        }  
	        // 创建计时器  
	        timer = new Timer(DELAY_TIME, new ActionListener() {  
	  
	            @Override  
	            public void actionPerformed(ActionEvent e) {  
	                changeCellStatus();  
	                repaint();  
	            }  
	        });  
	        // 开启计时器  
	        timer.start();  
	    }  
	  
	    /** 
	     * 复制地图 
	     *  
	 
	     */  
	    private void copyWorldMap() {  
	        for (int row = 0; row < nextStatus.length; row++) {  
	            for (int col = 0; col < nextStatus[row].length; col++) {  
	                tempStatus[row][col] = nextStatus[row][col];  
	            }  
	        }  
	    }  
